-- Corrigir todas as funções de update_at com search_path seguro

CREATE OR REPLACE FUNCTION public.update_clientes_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$;

